const value = 10.45;

console.log('After Math.round() '+ Math.round(value))    // 10

console.log('After Math.ceil() '+ Math.ceil(value))     //  11

console.log('After Math.floor() '+ Math.floor(value))      // 10

console.log('After Math.random() '+ Math.random(value))     // 0.6072798595402418

console.log('After Math.max() '+ Math.max(3, 56, 76, 3))    // 76

console.log('After Math.min() '+ Math.min(4, 3, 43, 55, 3))     // 3

console.log('After Math.pow() '+ Math.pow(value, 2))         // 109.20249999999999

console.log('After Math.sqrt() '+ Math.sqrt(value))          // 3.2326459750489227


